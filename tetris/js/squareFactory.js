function SquareFactory() {}
SquareFactory.prototype.make = function(index, dir) {
    var s;
    switch(index) {
        case 0:
            s = new Square1();
        break;
        case 1:
            s = new Square2();
        break;
        case 2:
            s = new Square3();
        break;
        case 3:
            s = new Square4();
        break;
        case 4:
            s = new Square5();
        break;
        case 5:
            s = new Square6();
        break;
        case 6:
            s = new Square7();
        break;
        default:
    }
    s.origin.x = 0;
    s.origin.y = 3;
    s.rotate(dir);
    return s;
}